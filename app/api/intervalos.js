import { basePath } from './config';

export function generateIntervaloApi(data, idConfigIntervalo, cantDictation, onlyValidation) {
    const url = `${basePath}/generate-intervalo?idConfigIntervalo=${idConfigIntervalo}&cantDictation=${cantDictation}&onlyValidation=${onlyValidation}`;
    const params = {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json',
        },
        body: JSON.stringify(data),
    };

    return fetch(url, params)
        .then((response) => {
            if (response.status === 501) {
                return null;
            } else {
                return response.json();
            }
        })
        .then((result) => {
            if (!result) {
                return {
                    ok: false,
                    message: 'Error interno del servidor.',
                };
            } else {
                if (result.ok) {
                    return {
                        ok: true,
                        intervalos: result.intervalos,
                        message: result.message,
                    };
                } else {
                    return {
                        ok: false,
                        message: result.message,
                    };
                }
            }
        })
        .catch((err) => {
            return {
                ok: false,
                message: 'Error de servidor, vuelva a intentarlo m치s tarde',
            };
        });
}

